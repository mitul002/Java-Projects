public class Rectangle {

    private int height;
    private int width;
    private int area;


    public void setHeight(int height)
    {
        this.height = height;
    }
    public int getHeight()
    {
        return height;
    }

    public void setWidth(int width)
    {
        this.width = width;
    }
    public int getWidth()
    {
        return width;
    }



    public void setArea(int area)
    {
        this.area = area;
    }
    public int getArea()
    {
        return area;
    }


}